package M3;

public class operators {
    public static void main(String[] args) {
        // arithmetic opr
        // declare variabel
        int a = 12, b = 5;

        // plus or add opr
        System.out.println(" a + b = " + (a + b));

        // minus opr
        System.out.println(" a - b = " + (a - b));

        // kali
        System.out.println(" a * b = " + (a * b));

        // bagi
        System.out.println(" a : b = " + (a / b));

        // modulo
        System.out.println(" a % b = " + (a % b));
        System.out.println("===================================");

        // Assignment opr
        // create variabel
        int c = 4;
        int var;

        // assign value using =
        var = c;
        System.out.println("Var using = : " + var);

        // assign value using =+
        var += c;
        System.out.println("Var using += : " + var);

        // assign value using *=
        var *= c;
        System.out.println("Var using *= " + var);


    }
}
